/*
	A java class about a point, with 2 getters, the getter of axis X and the getter of axis Y.
*/
public class Punt {
	private int x = 0;
	private int y = 0;
	public int getX() { return x; }
	public int getY() { return y; }
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
}
